#!/usr/bin/env bash
# Compose AGENTS.md, CLAUDE.md, GEMINI.md from selected modules.
# Usage: compose.sh /path/to/project
set -euo pipefail

source "$(dirname "${BASH_SOURCE[0]}")/_lib.sh"

target_dir=""
if [ "${1:-}" != "" ]; then
  target_dir="$(cd -- "$1" && pwd)"
else
  printf 'Usage: compose.sh /path/to/project\n' >&2
  exit 1
fi

manifest="$target_dir/.agent-os.yaml"
if [ ! -f "$manifest" ]; then
  printf 'error: no .agent-os.yaml found in %s\n' "$target_dir" >&2
  exit 1
fi

# Read modules from manifest
modules=()
while IFS= read -r _m; do
  [ -n "$_m" ] && modules+=("$_m")
done < <(_read_manifest_modules "$manifest")

if [ ${#modules[@]} -eq 0 ]; then
  printf 'error: no modules listed in manifest\n' >&2
  exit 1
fi

# Build the module list string for the header
module_list=$(printf '%s, ' "${modules[@]}")
module_list="${module_list%, }"
timestamp=$(date -u +"%Y-%m-%dT%H:%M:%SZ")

# ── Compose AGENTS.md ────────────────────────────────────────────────────────

agents_file="$target_dir/AGENTS.md"
{
  printf '<!-- Generated by my-agent-os. Do not edit manually. -->\n'
  printf '<!-- To customize, edit .agent-os/agents-extra.md instead. -->\n'
  printf '<!-- Modules: %s -->\n' "$module_list"
  printf '<!-- Generated: %s -->\n\n' "$timestamp"

  for mod in "${modules[@]}"; do
    mod_dir=$(_find_module_dir "$mod") || {
      printf 'warning: skipping unknown module: %s\n' "$mod" >&2
      continue
    }

    local_type=$(_module_type "$mod")
    fragment="$mod_dir/agents.md"
    [ -f "$fragment" ] || continue

    if [ "$mod" = "core" ]; then
      cat "$fragment"
      printf '\n'
    else
      printf '\n---\n\n'
      # Capitalize module name for heading
      display_name=$(echo "$mod" | sed 's/-/ /g' | awk '{for(i=1;i<=NF;i++) $i=toupper(substr($i,1,1)) substr($i,2)}1')
      printf '## %s (%s)\n\n' "$display_name" "$local_type"
      cat "$fragment"
      printf '\n'
    fi
  done

  # Append project-specific overrides
  local_extra="$target_dir/.agent-os/agents-extra.md"
  if [ -f "$local_extra" ] && [ -s "$local_extra" ]; then
    printf '\n---\n\n'
    printf '## Project-Specific Instructions\n\n'
    cat "$local_extra"
    printf '\n'
  fi
} > "$agents_file"

# ── Compose CLAUDE.md ────────────────────────────────────────────────────────

claude_file="$target_dir/CLAUDE.md"
cat > "$claude_file" <<'CLAUDE'
# Claude Adapter

Read `AGENTS.md` first. It is the canonical instruction file for this project.

Use Claude's native Skill tool when a local skill applies. If the Skill tool is unavailable, read the matching `skills/<name>/SKILL.md` file directly.

If this file conflicts with `AGENTS.md`, follow `AGENTS.md` unless the current user message says otherwise.
CLAUDE

# ── Compose GEMINI.md ────────────────────────────────────────────────────────

gemini_file="$target_dir/GEMINI.md"
cat > "$gemini_file" <<'GEMINI'
# Gemini Adapter

Read `AGENTS.md` first. It is the canonical instruction file for this project.

Use `activate_skill` when a local skill applies. If unavailable, read the matching `skills/<name>/SKILL.md` file directly.

If this file conflicts with `AGENTS.md`, follow `AGENTS.md` unless the current user message says otherwise.
GEMINI

printf 'composed: AGENTS.md (%d modules)\n' "${#modules[@]}"
